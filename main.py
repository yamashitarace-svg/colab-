def parse_date_jp(date_str):
    """日本語日付文字列をdatetime.dateに変換。失敗時はNone。"""
    m = re.search(r"令和(\d+)年(\d+)月(\d+)日", date_str)
    if m:
        return datetime.date(2018 + int(m.group(1)), int(m.group(2)), int(m.group(3)))
    m = re.search(r"(\d{4})年(\d+)月(\d+)日", date_str)
    if m:
        return datetime.date(int(m.group(1)), int(m.group(2)), int(m.group(3)))
    m = re.search(r"(\d{4})[/\-](\d{1,2})[/\-](\d{1,2})", date_str)
    if m:
        return datetime.date(int(m.group(1)), int(m.group(2)), int(m.group(3)))
    return None


def extract_period_end(period_text):
    """申込期間テキストから終了日を抽出。失敗時はNone。"""
    for pattern in [
        r"[〜～]\s*(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日|\d{4}/\d+/\d+)",
        r"(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日|\d{4}/\d+/\d+)\s*(?:まで|終了)",
    ]:
        m = re.search(pattern, period_text)
        if m:
            return parse_date_jp(m.group(1))
    return None


def extract_page_date(soup):
    """ページから公開日・更新日を抽出。失敗時は空文字。"""
    if soup is None:
        return ""
    text = soup.get_text(separator="\n")
    for pattern in [
        r"更新日[：:]\s*(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日)",
        r"公開日[：:]\s*(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日)",
        r"掲載日[：:]\s*(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日)",
        r"情報公開日[：:]\s*(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日)",
    ]:
        m = re.search(pattern, text)
        if m:
            return m.group(1)
    return ""


