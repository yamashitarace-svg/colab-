    for sib in boshu_heading.find_next_siblings():
        if sib.name in ["h2", "h3", "h4"] and "現在募集" not in sib.get_text():
            break
        tbl = sib if sib.name == "table" else sib.find("table")
        if tbl:
            for tr in tbl.find_all("tr")[1:]:
                cells = [td.get_text(strip=True) for td in tr.find_all(["td", "th"])]
                if any(c.strip() for c in cells):
                    cases.append(" | ".join(cells))
            if cases:
                break
        lst = sib if sib.name in ["ul", "ol"] else sib.find(["ul", "ol"])
        if lst:
            for li in lst.find_all("li"):
                t = li.get_text(strip=True)
                if t:
                    cases.append(t)
            if cases:
                break

    if cases:
        print(f"[{jichitai}] ✅ あり: {len(cases)}件")
        return [make_row(jichitai, "✅ あり", case, url=url, pub_date=pub_date) for case in cases]
