def parse_fujiidera():
    url = "https://www.city.fujiidera.lg.jp/soshiki/somubu/soumu/youchikanzai/1471333416329.html"
    jichitai = "藤井寺市"

    soup, err = fetch_page(url)
    if err:
        print(f"[{jichitai}] ⚠️ {err}")
        return [make_row(jichitai, "⚠️ 確認不可", f"アクセスエラー: {err}", url=url)]

    # ページ更新日を取得
    page_text = soup.get_text(separator="\n")
    pub_date = ""
    d_m = re.search(r"更新日[：:]\s*(令和\d+年\d+月\d+日|\d{4}年\d+月\d+日)", page_text)
    if d_m:
        pub_date = d_m.group(1)
    else:
        pub_date = extract_page_date(soup)

    # 「売却物件」見出し配下のテーブルを探す
    table = None
    for tag in soup.find_all(["h2", "h3", "h4", "h5", "p", "strong", "b"]):
        if "売却物件" in tag.get_text():
            for sib in tag.find_next_siblings():
                t = sib if sib.name == "table" else sib.find("table")
                if t:
                    table = t
                    break
                if sib.name in ["h2", "h3"]:
                    break
            break

    if table is None:
        for t in soup.find_all("table"):
            if any(kw in t.get_text() for kw in ["所在地", "地番", "売却"]):
                table = t
                break

    if table is None:
        if any(kw in page_text for kw in ["売却物件はありません", "現在、売却物件はありません"]):
            print(f"[{jichitai}] ❌ なし（ページ記載）")
            return [make_row(jichitai, "❌ なし・終了", "売却物件はありません（ページ記載）", url=url, pub_date=pub_date)]
        print(f"[{jichitai}] ⚠️ 売却物件テーブル読取不可")
        return [make_row(jichitai, "⚠️ 確認不可", "売却物件テーブル読取不可", url=url, pub_date=pub_date)]

    headers   = []
    rows_data = []
    for i, tr in enumerate(table.find_all("tr")):
        cells = [td.get_text(strip=True) for td in tr.find_all(["th", "td"])]
        if i == 0 and any(kw in " ".join(cells) for kw in ["所在地", "地番", "物件"]):
            headers = cells
        else:
            if any(c.strip() for c in cells):
                rows_data.append(cells)

    if not rows_data:
        print(f"[{jichitai}] ❌ テーブルにデータなし")
        return [make_row(jichitai, "❌ なし・終了", "売却物件テーブルにデータなし", url=url, pub_date=pub_date)]

    loc_idx = next((i for i, h in enumerate(headers) if "所在地" in h or "地番" in h), 0)
    locations = [row[loc_idx] for row in rows_data if loc_idx < len(row) and row[loc_idx].strip()]
    location_str = "・".join(locations[:3])

    # 申込期間を探す
    period = ""
    for pattern in [
        r"申込[み受付期間]*[：:]\s*(.+?)(?:\n|$)",
        r"受付期間[：:]\s*(.+?)(?:\n|$)",
        r"申込受付期間[：:]\s*(.+?)(?:\n|$)",
    ]:
        m = re.search(pattern, page_text)
        if m:
            period = m.group(1).strip()
            break

    if period:
        end_date = extract_period_end(period)
        if end_date:
            if end_date >= TODAY:
                status = "✅ あり"
                print(f"[{jichitai}] ✅ あり（受付中: {period}）")
            else:
                status = "❌ なし・終了"
                print(f"[{jichitai}] ❌ 受付終了（{period}）")
        else:
            status = "⚠️ 確認不可"
            print(f"[{jichitai}] ⚠️ 申込期間の終了日を特定できません: {period}")
    else:
        status = "⚠️ 確認不可"
        print(f"[{jichitai}] ⚠️ 申込期間が読み取れません")

    return [make_row(jichitai, status, "売却物件あり", location_str, period, url, pub_date)]


