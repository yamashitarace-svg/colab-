def _parse_osaka_city_section(soup, url, section_name, jichitai):
    section_heading = None
    for tag in soup.find_all(["h2", "h3", "h4", "h5", "p", "div", "strong", "span"]):
        if section_name in tag.get_text():
            section_heading = tag
            break

    if section_heading is None:
        print(f"[{jichitai}] ⚠️ セクション '{section_name}' が見つかりません")
        return [make_row(jichitai, "⚠️ 確認不可", "セクション見つからず", url=url)]

    section_nodes = []
    other_sections = {"契約管財局実施分", "水道局実施分"} - {section_name}
    for sib in section_heading.find_next_siblings():
        if any(kw in sib.get_text() for kw in other_sections):
            break
        section_nodes.append(sib)

    table    = None
    pub_date = ""
    for node in section_nodes:
        node_text = node.get_text()
        if not pub_date:
            d_m = re.search(r"令和\d+年\d+月\d+日|\d{4}年\d+月\d+日", node_text)
            if d_m:
                pub_date = d_m.group()
        candidate = node if node.name == "table" else node.find("table")
        if candidate and ("所在地" in candidate.get_text() or "物件" in candidate.get_text()):
            table = candidate
            break

    if table is None:
        section_text = " ".join(n.get_text() for n in section_nodes)
        if any(kw in section_text for kw in ["ありません", "ございません", "予定はありません"]):
            print(f"[{jichitai}] ❌ なし（ページ記載より）")
            return [make_row(jichitai, "❌ なし・終了", "現在案件なし（ページ記載）", url=url, pub_date=pub_date)]
        print(f"[{jichitai}] ⚠️ 土地テーブルが見つかりません")
        return [make_row(jichitai, "⚠️ 確認不可", "土地テーブル読取不可", url=url, pub_date=pub_date)]

    all_rows_data = []
    headers = []
    for i, tr in enumerate(table.find_all("tr")):
        cells = [td.get_text(strip=True) for td in tr.find_all(["th", "td"])]
        if i == 0:
            headers = cells
        else:
            if any(c.strip() for c in cells):
                all_rows_data.append(cells)

    if not all_rows_data:
        print(f"[{jichitai}] ❌ テーブルにデータ行なし")
        return [make_row(jichitai, "❌ なし・終了", "テーブルにデータなし", url=url, pub_date=pub_date)]

    loc_idx = next((i for i, h in enumerate(headers) if "所在地" in h or "住居表示" in h), 1)
    num_idx = next((i for i, h in enumerate(headers) if "物件番号" in h or "番号" in h), 0)

    result_rows = []
    all_locs = []
    for row in all_rows_data:
        loc = row[loc_idx] if loc_idx < len(row) else ""
        num = row[num_idx] if num_idx < len(row) else ""
        all_locs.append(loc)
        if is_target_area(loc):
            result_rows.append(make_row(jichitai, "✅ あり", f"物件番号: {num}", loc, url=url, pub_date=pub_date))

    if result_rows:
        print(f"[{jichitai}] ✅ あり: 対象エリア {len(result_rows)}件")
        return result_rows
    elif all_locs:
        print(f"[{jichitai}] ❌ 対象エリア外 ({len(all_locs)}件確認)")
        return [make_row(jichitai, "❌ なし・終了", f"物件あり・対象エリア外 ({len(all_locs)}件)", url=url, pub_date=pub_date)]
    else:
        return [make_row(jichitai, "❌ なし・終了", "物件情報なし", url=url, pub_date=pub_date)]


