def parse_osaka_pref1():
    url = "https://www.pref.osaka.lg.jp/o050050/kanzai/baikyakutou/index.html"
    jichitai = "大阪府1（財産活用課）"

    soup, err = fetch_page(url)
    if err:
        print(f"[{jichitai}] ⚠️ {err}")
        return [make_row(jichitai, "⚠️ 確認不可", f"アクセスエラー: {err}", url=url)]

    pub_date = extract_page_date(soup)

    # 「新着情報」見出しを探す
    shinchaku = None
    for tag in soup.find_all(["h2", "h3", "h4", "h5", "p", "div", "span"]):
        if "新着情報" in tag.get_text(strip=True):
            shinchaku = tag
            break

    if shinchaku is None:
        print(f"[{jichitai}] ⚠️ 新着情報セクションが見つかりません")
        return [make_row(jichitai, "⚠️ 確認不可", "新着情報セクション見つからず", url=url, pub_date=pub_date)]

    # セクション配下のリンクを収集
    topics = []
    container = shinchaku.parent
    for a in container.find_all("a"):
        title = a.get_text(strip=True)
        href  = a.get("href", "")
        if title and href:
            topics.append({"title": title, "href": href})

    if not topics:
        for sibling in shinchaku.find_next_siblings():
            if sibling.name in ["h2", "h3"] and sibling != shinchaku:
                break
            for a in sibling.find_all("a"):
                title = a.get_text(strip=True)
                href  = a.get("href", "")
                if title:
                    topics.append({"title": title, "href": href})

    if not topics:
        print(f"[{jichitai}] ⚠️ トピック読取不可")
        return [make_row(jichitai, "⚠️ 確認不可", "トピック読取不可", url=url, pub_date=pub_date)]

    ACTIVE_KW = ["募集", "入札公告", "公募", "売払", "公売", "処分", "売却"]
    RESULT_KW = ["結果", "落札", "成約"]

    active_topics = [
        t for t in topics
        if any(kw in t["title"] for kw in ACTIVE_KW)
        and not any(kw in t["title"] for kw in RESULT_KW)
    ]

    if not active_topics:
        print(f"[{jichitai}] ❌ なし（結果系トピックのみ、または該当なし）")
        return [make_row(jichitai, "❌ なし・終了", "結果系トピックのみまたは該当なし", url=url, pub_date=pub_date)]

    rows = []
    for t in active_topics:
        href = t["href"]
        if href and not href.startswith("http"):
            href = "https://www.pref.osaka.lg.jp" + href
        detail_soup, _ = fetch_page(href)
        time.sleep(1)
        location = ""
        period   = ""
        detail_pub = pub_date
        if detail_soup:
            text = detail_soup.get_text(separator="\n")
            m = re.search(r"所在地[：:]\s*(.+?)(?:\n|$)", text)
            if m:
                location = m.group(1).strip()
            m = re.search(r"(?:申込|受付)期間[：:]\s*(.+?)(?:\n|$)", text)
            if m:
                period = m.group(1).strip()
            detail_pub = extract_page_date(detail_soup) or pub_date
        rows.append(make_row(jichitai, "✅ あり", t["title"], location, period, href, detail_pub))

    print(f"[{jichitai}] ✅ あり: {len(rows)}件")
    return rows


