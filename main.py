def fetch_page(url, timeout=15):
    """URLからHTMLを取得しBeautifulSoupオブジェクトを返す。失敗時は (None, エラーメッセージ) を返す。"""
    try:
        resp = requests.get(url, headers=HEADERS, timeout=timeout)
        resp.raise_for_status()
        detected = chardet.detect(resp.content)
        enc = (detected.get("encoding") or "utf-8").lower()
        if enc in ("shift_jis", "shift-jis", "sjis", "cp932"):
            resp.encoding = "cp932"
        elif enc in ("euc-jp", "euc_jp"):
            resp.encoding = "euc-jp"
        else:
            resp.encoding = enc
        return BeautifulSoup(resp.text, "html.parser"), None
    except requests.exceptions.Timeout:
        return None, "タイムアウト"
    except requests.exceptions.ConnectionError:
        return None, "接続エラー"
    except requests.exceptions.HTTPError as e:
        return None, f"HTTPエラー {e.response.status_code}"
    except Exception as e:
        return None, f"エラー: {e}"


